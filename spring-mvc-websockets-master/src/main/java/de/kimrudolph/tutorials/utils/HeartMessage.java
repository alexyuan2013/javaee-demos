package de.kimrudolph.tutorials.utils;

public class HeartMessage {
	
	private String T;
	private String D;
	private String V;
	public String getT() {
		return T;
	}
	public void setT(String t) {
		T = t;
	}
	public String getD() {
		return D;
	}
	public void setD(String d) {
		D = d;
	}
	public String getV() {
		return V;
	}
	public void setV(String v) {
		V = v;
	}
	
	

}
